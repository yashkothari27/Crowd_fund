window.onload = async () => {
  if (window.ethereum) {
    provider = new ethers.BrowserProvider(window.ethereum);
    await window.ethereum.request({ method: 'eth_requestAccounts' });
    signer = await provider.getSigner();
    contract = new ethers.Contract(contractAddress, abi, signer);
  } else {
    alert("Install MetaMask");
  }
};

async function contribute() {
  const amount = document.getElementById("amount").value;
  const token = new ethers.Contract(tokenAddress, [
    "function approve(address spender, uint amount) public returns (bool)"
  ], signer);

  const decimals = 18; // adjust based on your token
  const amt = ethers.parseUnits(amount, decimals);

  await token.approve(contractAddress, amt);
  await contract.contribute(amt);
}

async function claimFunds() {
  await contract.claimFunds();
}

async function refund() {
  await contract.refund();
}
